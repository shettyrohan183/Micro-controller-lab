		AREA ZEROS,CODE,READONLY
NUMBER     RN 1
NUMONES    RN 10
NUMZEROS   RN 11
ENTRY
		 MOV R5,#0x40000000
		 LDR NUMBER,=0xB
		 MOV NUMONES,#0
		 MOV NUMZEROS,#0
LOOP     LSRS NUMBER,#1
		 ADDCS NUMONES,#1
		 ADDCC NUMZEROS,#1
		 CMP NUMBER,#0
		 BNE LOOP
		 STR NUMONES,[R5]
		 STR NUMZEROS,[R5,#4]
STOP	 B STOP
		 END